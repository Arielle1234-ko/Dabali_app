class EthnicCuisine {
  final String id;
  final String name;
  final String group;
  final List<String> regions;
  final String description;
  final String historicalOverview;
  final String culinaryIdentity;
  final List<String> communities;
  final List<String> specialties;
  final List<String> stapleIngredients;
  final List<String> culturalMoments;
  final List<String> recipeIds;

  const EthnicCuisine({
    required this.id,
    required this.name,
    required this.group,
    required this.regions,
    required this.description,
    required this.historicalOverview,
    required this.culinaryIdentity,
    required this.communities,
    required this.specialties,
    required this.stapleIngredients,
    required this.culturalMoments,
    required this.recipeIds,
  });
}
